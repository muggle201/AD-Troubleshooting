Import-LocalizedData -BindingVariable ScriptStrings


#Networking Information - NIC Cards
$NIC_Configs = Get-CimInstance Win32_NetworkAdapterConfiguration -Filter "IPEnabled = true"
$NetworkAdapter_Summary = New-Object PSObject

Foreach ($NIC_Config in $NIC_Configs)
{
	$NIC_Config_Summary = New-Object PSObject

	$NetworkAdapter = Get-CimInstance Win32_NetworkAdapter -Filter ("DeviceID = " + $NIC_Config.Index)
	if ($null -ne $NetworkAdapter)
	{
		$NIC_Config_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_NetworkAdapter -Value $NetworkAdapter.Name
		#$NIC_Config_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_NetworkAdapterType -Value ($NetworkAdapter.AdapterType)
		$NIC_Config_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_NetworkAdapterSpeed -Value ("{0:N0}" -f $NetworkAdapter.Speed)
		$ConnectionID = $NetworkAdapter.NetConnectionID
	}
	else
	{
		$ConnectionID = $NIC_Config.Description
	}
	
	$NIC_Config_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_IPAddresses -Value ([string]::Join("<br/>", $NIC_Config.IPAddress))
	$NIC_Config_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_DefaultGateway -Value ([string]::Join("<br/>", $NIC_Config.DefaultIPGateway))
	$NIC_Config_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_DNSServers -Value ([string]::Join("<br/>", $NIC_Config.DNSServerSearchOrder))
	
	$NetworkAdapter_Summary  | Add-Member -MemberType NoteProperty -Name $ConnectionID -Value (Convert-PSObjectToHTMLTable -PSObject $NIC_Config_Summary)
}

$NetworkAdapter_Summary | ConvertTo-Xml2 | update-diagreport -id 11_Nic -name "Network Adapter Configuration" -verbosity informational

$IESettings_Summary = New-Object PSObject

$IESettings = (get-itemproperty 'HKCU:\Software\Microsoft\Windows\CurrentVersion\Internet Settings')

if ($IESettings.ProxyEnable -eq 1)
{
	$IESettings_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_ProxyEnabled -Value $ScriptStrings.ID_Enabled
	$IESettings_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_ProxyServerAdd -Value $IESettings.ProxyServer
	
	if ($null -ne $IESettings.ProxyOverride)
	{
		$IESettings_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_ProxyOverride -Value (Replace-XMLChars $IESettings.ProxyOverride)
	}
}
else
{
	$IESettings_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_ProxyEnabled -Value $ScriptStrings.ID_Disabled
}

$IESettingsConnections = (get-itemproperty 'HKCU:\Software\Microsoft\Windows\CurrentVersion\Internet Settings\Connections')
if ($null -ne $IESettingsConnections.DefaultConnectionSettings)
{
	#Possible Values
	#01 - neither of those 2 auto config boxes are checked 
	#05 - just the Use automatic configuration script is checked 
	#09 - just the Automatically detect settings is checked 
	#0d - both of them are checked 
	$DefaultConnectionSetting = $IESettingsConnections.DefaultConnectionSettings[8]
		
	$AutoEnabledValues = @(9, 11, 13, 15)
	if ($AutoEnabledValues -contains $DefaultConnectionSetting)
	{
		$IESettings_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_ProxyAutoSettings -Value $ScriptStrings.ID_Enabled
	}
	elseif ($DefaultConnectionSetting -eq 1)
	{
		$IESettings_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_ProxyAutoSettings -Value $ScriptStrings.ID_Disabled
	}
}

if ($null -ne $IESettings.AutoConfigURL)
{
	$IESettings_Summary | Add-Member -MemberType NoteProperty -Name $ScriptStrings.ID_MATS_ProxyAutoScript -Value $IESettings.AutoConfigURL
}


$IESettings_Summary | ConvertTo-Xml2 | update-diagreport -id 11_IE -name "Internet Explorer Configuration" -verbosity informational



