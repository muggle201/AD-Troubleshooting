# Description: 
# 		Collects SQL Error Logs from the SQL Server hosting the ConfigMgr database. It creates a UNC Path to the 
#		SQL Error Logs so logs can be collected remotely.
# *********************************************************************************************************************

trap [Exception] 
{ 
	WriteTo-ErrorDebugReport -ErrorRecord $_
	continue 
}

TraceOut "Started"
Import-LocalizedData -BindingVariable ScriptStrings
$sectiondescription = "Configuration Manager Logs"
$SQLServerName = $ConfigMgrDBServer
$ZipName = $ComputerName + "_Logs_SQLError.zip"
$fileDescription = "SQL Error Logs"

If ($Is_SiteServer)
{	
	# ----------------
	# Write Progress
	# ----------------
	Write-DiagProgress -Activity "SQL Logs" -Status "Collecting SQL Logs"
	
	$SQLServerKey = "SOFTWARE\Microsoft\Microsoft SQL Server\Instance Names\SQL"	
	$compress = $false
		
	# Get instance name for Database Server
	If ($ConfigMgrDBName.Contains("\")) {
		$instance = $ConfigMgrDBName.Substring(0,$ConfigMgrDBName.IndexOf("\"))
		$ConfigMgrDBName = $ConfigMgrDBName.Substring($ConfigMgrDBName.IndexOf("\")+1)
		$SQLInstanceName = $instance		# Named instance
	}
	Else {
		$SQLInstanceName ="MSSQLSERVER"		# Default instance
	}
	
	trap [Exception] {		
		TraceOut "    Failed to access $SQLServerKey. Path does not exist or Access Denied"
		AddTo-CMServerSummary "SQL Error Logs" -Value "Failed to access $SQLServerKey. Path does not exist or Access Denied" -NoToSummaryReport		
		return
	}
	
	$RegKey = [Microsoft.Win32.RegistryKey]::OpenRemoteBaseKey("LocalMachine", $SQLServerName)
	$Key = $RegKey.OpenSubKey($SQLServerKey)
	$SQLInstance = $Key.GetValue($SQLInstanceName)	
	
	$SQLServerInstanceKey = "SOFTWARE\Microsoft\Microsoft SQL Server\" + $SQLInstance + "\MSSQLServer\Parameters"

	$RegKey = [Microsoft.Win32.RegistryKey]::OpenRemoteBaseKey("LocalMachine", $SQLServerName)
	$Key = $RegKey.OpenSubKey($SQLServerInstanceKey)

	$KeyArgs = $Key.GetValueNames()
	foreach ($Arg in $KeyArgs)
	{
	    $value = $Key.GetValue($Arg)
	    if ($value -match '^-e(.+)') 
	    {	
			# -e Argument specifies the Error Log Path. $matches is set by -match and 1st element stores the second match, which is the path.
	        $SqlServerLogPath = [System.IO.Path]::GetDirectoryName($matches[1])         
	        TraceOut "    SQL Error Log Path = $SqlServerLogPath"
	        break
	    }    
	}
	
	# Convert Log Path to UNC Path
	$SqlServerLogPathUNC = $SqlServerLogPath -replace ":","$"
	$SqlServerLogPathUNC = "\\$SQLServerName\" + $SqlServerLogPathUNC
	TraceOut "    SQL Server Log Path UNC = $SqlServerLogPathUNC"

	# Create Temporary Destination folder to copy the logs in \Windows\Temp
	$Destination = Join-Path $Env:windir ("\Temp\" + $SQLServerName + "_Logs_SQLError")
	If (Test-Path $Destination)
	{
		Remove-Item -Path $Destination -Recurse -Force
	}	
	New-Item -ItemType "Directory" $Destination | Out-Null #_#
	
	If (Get-ChildItem $SqlServerLogPathUNC -ErrorAction SilentlyContinue -ErrorVariable AccessError)
	{
		Copy-Item $SqlServerLogPathUNC\ERRORLOG $Destination -ErrorAction SilentlyContinue
		Copy-Item $SqlServerLogPathUNC\ERRORLOG.1 $Destination -ErrorAction SilentlyContinue
		Copy-Item $SqlServerLogPathUNC\ERRORLOG.2 $Destination -ErrorAction SilentlyContinue
		Copy-Item $SqlServerLogPathUNC\*.mtxt $Destination -ErrorAction SilentlyContinue
		# Copy-Item $SqlServerLogPathUNC\*.mdmp $Destination -ErrorAction SilentlyContinue
		$compress = $true
	}
	Else
	{
		TraceOut "    Failed to access $SqlServerLogPathUNC. Path does not exist or Access Denied"
		# AddTo-CMServerSummary "SQL Error Logs" -Value "Failed to access $SqlServerLogPathUNC. Path does not exist or Access Denied" -NoToSummaryReport
		AddTo-CMDatabaseSummary -Name "SQL Error Logs" -Value "Failed to access $SqlServerLogPathUNC. Path does not exist or Access Denied" -NoToSummaryQueries -NoToSummaryReport
	}
	
	If ($compress)
	{
		Write-DiagProgress -Activity "SQL Logs" -Status "Compressing SQL Logs"
		compressCollectFiles -DestinationFileName $ZipName -filesToCollect ($Destination + "\*.*") -sectionDescription $sectionDescription -fileDescription $fileDescription -Recursive -ForegroundProcess -noFileExtensionsOnDescription
		Remove-Item -Path $Destination -Recurse
		# AddTo-CMServerSummary "SQL Error Logs" -Value "Review $ZipName" -NoToSummaryReport
		AddTo-CMDatabaseSummary -Name "SQL Error Logs" -Value "Review $ZipName" -NoToSummaryQueries -NoToSummaryReport
	}
	
}

Traceout "Completed"


