Display-DefaultActivity -File -FileName "pfirewall.log"


if ($OSVersion.Major -ge 6)  #Windows 7 and Windows 8 OSs
{
	$AllPaths=(
	       "HKLM:\SYSTEM\CurrentControlSet\services\SharedAccess\Defaults\FirewallPolicy\DomainProfile\Logging",
	       "HKLM:\SYSTEM\CurrentControlSet\services\SharedAccess\Defaults\FirewallPolicy\PublicProfile\Logging",
	       "HKLM:\SYSTEM\CurrentControlSet\services\SharedAccess\Defaults\FirewallPolicy\StandardProfile\Logging",
	       "HKLM:\SYSTEM\CurrentControlSet\services\SharedAccess\Parameters\FirewallPolicy\DomainProfile\Logging",
	       "HKLM:\SYSTEM\CurrentControlSet\services\SharedAccess\Parameters\FirewallPolicy\PublicProfile\Logging",
	       "HKLM:\SYSTEM\CurrentControlSet\services\SharedAccess\Defaults\FirewallPolicy\StandardProfile\Logging",
	       "HKLM:\SOFTWARE\Policies\Microsoft\WindowsFirewall\DomainProfile\Logging",
	       "HKLM:\SOFTWARE\Policies\Microsoft\WindowsFirewall\PublicProfile\Logging",
	       "HKLM:\SOFTWARE\Policies\Microsoft\WindowsFirewall\StandardProfile\Logging"
		   )
		   
    foreach($Path in $AllPaths)
	{  
	   if(Test-Path -Path $Path)
	   {
	      $LogLocation=(Get-ItemProperty -Path $Path).LogFilePath 
		  if ( ($null -ne $LogLocation) -and ($LogLocation.EndsWith("pfirewall.log")) )
		  {
		     if(Test-Path -Path $LogLocation)
		     {
		        CollectFiles -filesToCollect $LogLocation -fileDescription "Windows Firewall Security Log" -sectionDescription "Windows Firewall Logs" -renameOutput $true 
			    break
		     }
		  }
	   }
	}
}
	

