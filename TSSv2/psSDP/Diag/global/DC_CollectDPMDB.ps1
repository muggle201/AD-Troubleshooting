Import-LocalizedData -BindingVariable LocalsCollectDPMDB -FileName DC_CollectDPMDB -UICulture en-us

. ./Utils_CTS.ps1
. ./TS_RemoteSetup.ps1

function GetDPMSQLPath
{
	if ((Test-Path "HKLM:SOFTWARE\Microsoft\Microsoft Data Protection Manager\DB") -eq $true)
	{
		(get-itemproperty HKLM:\SOFTWARE\Microsoft\"Microsoft Data Protection Manager"\DB).SqlServer
	} Else {
		$null
	}
}

function GetSQLInstance
{
	if ((Test-Path "HKLM:SOFTWARE\Microsoft\Microsoft Data Protection Manager\DB") -eq $true)
	{
		(get-itemproperty HKLM:\SOFTWARE\Microsoft\"Microsoft Data Protection Manager"\DB).InstanceName
	} Else {
		$null
	}
}

$DPMSqlServer = GetDPMSQLPath;
If ($null -eq $DPMSqlServer)
{
	exit 1
}

Write-DiagProgress -Activity $LocalsCollectDPMDB.ID_DPM_DPMDB_ACTIVITY -Status $LocalsCollectDPMDB.ID_DPM_DPMDB_STATUS

$OutputBase= "$ComputerName$Prefix" + "_LOCAL_DPMDB"
if ($ComputerName -eq $DPMSqlServer)
{
	#SQLServer is local on DPM server
	$DPMBinDir = GetDPMInstallFolder 
	if ($null -eq $DPMBinDir)
	{
		$LocalsCollectDPMDB.ID_DPM_NotInstalled | WriteTo-StdOut -ShortFormat
		exit 1
	}

	#Get the DPM directory then run dpmbackup.exe from there to compress
	$DPMBackupFolder = $DPMBinDir
	$DPMBinDir = Join-Path $DPMBinDir "bin"
	$DPMBackupCmd = $DPMBinDir + "\DPMBackup.exe"
	$DPMBackupFolder = Join-Path $DPMBackupFolder "Volumes\ShadowCopy\Database Backups"
	$DPMBackupFile = $DPMBackupFolder + "\DPMDB.bak"
	if($debug -eq $true){[void]$shell.popup($DPMBackupFile)}

	# Check if dpmdb.bak exist. If so delete it
	while (Test-Path $DPMBackupFile)
	{
		remove-item $DPMBackupFile
	}

	$processStartInfo  = new-object System.Diagnostics.ProcessStartInfo
	$processStartInfo.fileName = $DPMBackupCmd
	$processStartInfo.Arguments = "-db"
	$processStartInfo.UseShellExecute = $false
	$processStartInfo.RedirectStandardOutput = $true
	$processStartInfo.RedirectStandardError = $true

	$process = [System.Diagnostics.Process]::Start($processStartInfo)
	$DPMDBOutput = $OutputBase
	$DPMDBInput = $DPMBackupFile	
	Run-DiagExpression .\DC_BackgroundCollector.ps1 -process $process -filesToCollect $DPMDBInput -outputFiles $DPMDBOutput
}
else
{
	if($debug -eq $true){[void]$shell.popup("Running remote connection to SQL")}
	$RemoteInstance = GetSQLInstance
	$ScriptExpression = ".\DC_CollectDPMDBRemote.ps1 -SqlInstanceName " + $RemoteInstance
	ExecuteRemoteExpression -ComputerNames $DPMSqlServer -Expression $ScriptExpression -ShowDialog
}


