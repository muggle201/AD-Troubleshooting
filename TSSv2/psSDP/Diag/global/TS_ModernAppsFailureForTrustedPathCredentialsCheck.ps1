Import-LocalizedData -BindingVariable ScriptStrings
Display-DefaultActivity -Rule -RuleNumber 7727

$RuleApplicable = $false
$RootCauseDetected = $false
$RootCauseName = "RC_ModernAppsFailureForTrustedPathCredentialsCheck"
$InformationCollected = new-object PSObject

# ***************************
# Data Gathering
# ***************************

Function AppliesToSystem
{
	#win 8
	return (($OSVersion.Major -eq 6) -and ($OSVersion.Minor -eq 2))
}

Function CheckTrustedPathForCredentialState()
{
	$groupPolicyPath = "HKLM:\Software\Microsoft\Windows\CurrentVersion\Policies\CredUI"
	if(Test-Path $groupPolicyPath)
	{
		$groupPolicy = Get-ItemProperty $groupPolicyPath
		if($groupPolicy.EnableSecureCredentialPrompting -eq 1)
		{
			#Add information to InformationCollected
			$InformationCollected | add-member -membertype noteproperty -name "Require trusted path for credential entry" -value "Enabled"
			return $true
		}
	}
	return $false
}

# **************
# Detection Logic
# **************

#Check to see if rule is applicable to this computer
if (AppliesToSystem)
{
	$RuleApplicable = $true
	$RootCauseDetected = CheckTrustedPathForCredentialState	
}	
	

# *********************
# Root Cause processing
# *********************

if ($RuleApplicable)
{
	if ($RootCauseDetected)
	{
		# Red/ Yellow Light
		Update-DiagRootCause -id $RootCauseName -Detected $true
		Add-GenericMessage -Id $RootCauseName -InformationCollected $InformationCollected
	}
	else
	{
		# Green Light
		Update-DiagRootCause -id $RootCauseName -Detected $false
	}
}
